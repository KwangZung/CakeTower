SDL_Texture* loadTexture (string path, SDL_Renderer* renderer)
{
    SDL_Texture* newTexture = nullptr;
    SDL_Surface* loadedSurface = IMG_Load (path.c_str());
    if (loadedSurface == nullptr)
        cout << "Unable to load image" << path << " SDL_image Error; "
             << IMG_GetError() << "\n";
    else {
        newTexture = SDL_CreateTextureFromSurface (renderer, loadedSurface);
        if (newTexture==nullptr)
            cout << "Unable to create texture from " << path << " SDL Error: "
                 << SDL_GetError() << "\n";
        SDL_FreeSurface(loadedSurface);
    }
    return newTexture;
}

SDL_Surface* loadIcon(const char* path) {
    SDL_Surface* icon = SDL_LoadBMP(path); // Tải icon từ tập tin BMP
    if (!icon) {
        SDL_Log("Unable to load icon: %s", SDL_GetError());
        return NULL;
    }
    return icon;
}

void renderTexture(SDL_Texture *texture, int x, int y,
                   SDL_Renderer* renderer)
{
	SDL_Rect dest;
	dest.x = x;
	dest.y = y;
	SDL_QueryTexture(texture, NULL, NULL, &dest.w, &dest.h);

	SDL_RenderCopy(renderer, texture, NULL, &dest);
}

// Các hàm chung về khởi tạo và huỷ SDL
void logSDLError(std::ostream& os,
                 const std::string &msg, bool fatal)
{
    os << msg << " Error: " << SDL_GetError() << std::endl;
    if (fatal) {
        SDL_Quit();
        exit(1);
    }
}

void initSDL(SDL_Window* &window, SDL_Renderer* &renderer)
{
    if (SDL_Init(SDL_INIT_EVERYTHING) != 0)
        logSDLError(std::cout, "SDL_Init", true);

    window = SDL_CreateWindow(WINDOW_TITLE.c_str(), SDL_WINDOWPOS_CENTERED,
       SDL_WINDOWPOS_CENTERED, SCREEN_WIDTH, SCREEN_HEIGHT, SDL_WINDOW_SHOWN);
    //window = SDL_CreateWindow(WINDOW_TITLE.c_str(), SDL_WINDOWPOS_CENTERED,
    //   SDL_WINDOWPOS_CENTERED, SCREEN_WIDTH, SCREEN_HEIGHT, SDL_WINDOW_FULLSCREEN_DESKTOP);
    if (window == nullptr) logSDLError(std::cout, "CreateWindow", true);


    //Khi thông thường chạy với môi trường bình thường ở nhà
    renderer = SDL_CreateRenderer(window, -1, SDL_RENDERER_ACCELERATED |
                                              SDL_RENDERER_PRESENTVSYNC);
    //Khi chạy ở máy thực hành WinXP ở trường (máy ảo)
    //renderer = SDL_CreateSoftwareRenderer(SDL_GetWindowSurface(window));
    if (renderer == nullptr) logSDLError(std::cout, "CreateRenderer", true);

    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "linear");
    SDL_RenderSetLogicalSize(renderer, SCREEN_WIDTH, SCREEN_HEIGHT);
}

void quitSDL(SDL_Window* window, SDL_Renderer* renderer)
{
	SDL_DestroyRenderer(renderer);
	SDL_DestroyWindow(window);
	SDL_Quit();
}

SDL_Rect SetRect (int w, int h, int x, int y)
{
    SDL_Rect rec;
    rec.h = h;
    rec.w = w;
    rec.x = x;
    rec.y = y;
    return rec;
}

int mid_x (SDL_Texture* texture)
{
    int* w = new int;
    SDL_QueryTexture(texture, NULL, NULL, w, NULL);
    int x = (SCREEN_WIDTH - *w)/2;
    delete w;
    return x;
}

void waitUntilKeyPressed()
{
    SDL_Event e;
    while (true) {
        if ( SDL_WaitEvent(&e) != 0 &&
             (e.type == SDL_KEYDOWN || e.type == SDL_QUIT) )
            return;
        SDL_Delay(100);
    }
}
